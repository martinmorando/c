/*
    Printf
    - %i: integer
    - %f: float or double
    - %c: char
*/

#include <stdio.h>

int main() {

    // Integer
    int age = 20;
    printf("%i\n", age);

    // Float or double
    float price = 16.79;
    printf("%f\n", price);

    // Char
    char letter = 'C';
    printf("%c\n", letter);

}