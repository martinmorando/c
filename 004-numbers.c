/*
    Numbers
*/

#include <stdio.h>

int main() {

    int a = 1;
    int b = 2;


    // [BASIC OPERATIONS]
    a + b;          // 3
    a - b;          // 1
    a * b;          // 2
    a / b;          // 2
    a % b;          // 0


    // [COMPOUND ASSIGNMENT OPERATORS]
    a += 3;         // 4    
    a -= 2;         // 2    
    a *= 2;         // 4    
    a /= 2;         // 2    
    a %= 2;         // 0


    // [INCREMENT & DECREMENT OPERATORS]
    a++;            // 2
    b--;            // 1

}